const mint = async (contractName: string) => {
  const [owner] = await ethers.getSigners();

  const addressList = await addressUtils.getAddressList(hre.network.name);
  const token = MintableToken__factory.connect(
    addressList[contractName],
    owner
  );
  const oldToken = MintableToken__factory.connect(
    oldAddresses[contractName],
    owner
  );

  let x = 0;

  for (let i = 0; i < addresses.length; i++) {
    const txCount = await owner.getTransactionCount("latest");
    const promises = addresses
      .slice(i * WINDOW_SIZE, (i + 1) * WINDOW_SIZE)
      .map(async (address, index) => {
        const nonce = txCount + x++;
        const oldBalance = await oldToken.balanceOf(address);
        const newBalance = await token.balanceOf(address);
        if (!oldBalance.eq(0) && newBalance.eq(0)) {

          console.log({ nonce });

          await token
            .mint(address, oldBalance.mul(100), { nonce, gasPrice: '15000000000' })
            .then((tx) => tx.wait());

          console.log(
            contractName,
            "Balance: ",
            await token.balanceOf(address).then((res) => formatEther(res))
          );
        }
      });
    await Promise.all(promises);
  }

  console.log("Complete");
};

